package CentroExpress;

public class Estudio {
	
	private String nombreDelEstudio;
	private int codigoDelEstudio;
	private int costoDelEstudio;
	
	public Estudio(String nombreDelEstudio, int codigoDelEstudio, int costoDelEstudio) {
		this.nombreDelEstudio = nombreDelEstudio;
		this.codigoDelEstudio = codigoDelEstudio;
		this.costoDelEstudio = costoDelEstudio;
	}
	
	
	
}
